-- Bonne configuration ! :D
------

 easyhud = easyhud or {} -- Ne touchez pas à ça, s'il vous plaît
 
------

 easyhud.icon1 = "easyhud/licenseb.png" -- Îcone pour la license d'arme

 easyhud.licensewepicon = false -- Affiche une îcone si le joueur possède une license d'arme. Mettez ça a false pour le desactiver.
 
 easyhud.icon2 = "easyhud/wantedb.png" -- Îcone quand vous êtes recherché par la police

 easyhud.wantedicon = true -- Affiche une îcone si le joueur est recherché par la police. Mettez ça a false pour le desactiver.
 
------

 easyhud.health = true -- Barre de vie. Mettez ça a false pour la desactiver.

 easyhud.colorhealthsuf = Color(255, 255, 255, 255) -- Couleur pour le suffixe de la barre de vie

 easyhud.colorhealth = Color(178, 68, 110, 255)  -- Couleur pour la barre de vie

------

 easyhud.food = true -- Barre de nourriture. Mettez ça a false pour la desactiver.

 easyhud.colorfoodsuf = Color(255, 255, 255, 255) -- Couleur pour le suffixe de la barre de nourriture

 easyhud.colorfood = Color(255, 242, 112, 255) -- Couleur pour la barre de nourriture

------

 easyhud.armor = true -- Barre d'armure. Mettez ça a false pour la desactiver.

 easyhud.colorarmorsuf = Color(255, 255, 255, 255) -- Couleur pour le suffixe de la barre d'armure

 easyhud.colorarmor = Color(103, 164, 178, 255) -- Couleur pour la barre d'armure

------

 easyhud.nameover = "Wanka RP" -- Le nom de votre serveur sur l'hud

 easyhud.name = false--- Mettez ça a false si vous n'en voulez pas.

 easyhud.colorname = Color(255, 222, 218, 255) -- Couleur pour le nom de votre serveur

------

 easyhud.suffixes = true -- Mettez ça a false si vous ne voulez AUCUN suffixe

------

 easyhud.sufhealth = "Vie" -- Suffixe de la vie

 easyhud.sufhealthstate = true -- Mettez ça a false si vous n'en voulez pas.

------

 easyhud.sufarmor = "Armure" -- Suffixe de l'armure

 easyhud.sufarmorstate = true -- Mettez ça a false si vous n'en voulez pas.

------

 easyhud.suffood = "Nourriture" -- Suffixe de la nourriture

 easyhud.suffoodstate = true -- Mettez ça a false si vous n'en voulez pas.

------

 easyhud.playername = false -- Mettez ça a false si vous ne voulez pas afficher le nom du joueur sur l'hud.

 easyhud.playernamecolor = Color(255, 255, 255, 255) -- Définit la couleur du nom du joueur sur l'hud

 easyhud.playerjob = true --  Mettez ça a false si vous ne voulez pas afficher le métier du joueur sur l'hud.
 
 easyhud.playerjobcolor = Color(255, 255, 255, 255) -- Définit la couleur du métier du joueur sur l'hud

------

 easyhud.currency = "€" -- Définis la devise de la monnaie sur l'hud.

 easyhud.playermoney = true -- Mettez ça a false si vous ne voulez pas afficher la monnaie du joueur sur l'hud.

 easyhud.boxmoney = false -- Mettez ça a false si vous ne voulez pas de rectangle autour de la monnaie du joueur.

 easyhud.boxcolor1 = Color(103, 164, 178, 255) -- Définit la couleur de la bordure du rectangle 

 easyhud.boxcolor2 = Color(0, 0, 0, 200) -- Définit la couleur du centre du rectangle 

 easyhud.playermoneycolor = Color(255, 242, 112, 255)  -- Définit la couleur de la monnaie affichée sur l'écran.

------

 easyhud.consolesmessages = true -- Affiche des messages dans la console quand le serveur charge.

------













